class _ClusterGeometryMixin:
    """
    Shared geometry helpers for all cluster types.

    Provides size/span/boundary/overlap logic based solely on the
    ``positions`` tuple, so the three cluster dataclasses
    (:class:`Cluster`, :class:`HydrophobicAromaticCluster`,
    :class:`MultiRuleCluster`) expose an identical geometric interface
    without duplicating code.

    Declares an empty ``__slots__`` so subclassing frozen, slotted
    dataclasses preserves their memory layout (no ``__dict__`` added).
    """
    __slots__ = ()

    @property
    def size(self) -> int:
        """Number of residues in the cluster."""
        return len(self.positions)

    @property
    def span(self) -> int:
        """Sequence span from first to last position (inclusive)."""
        return max(self.positions) - min(self.positions) + 1 if self.positions else 0

    @property
    def min_position(self) -> int:
        """N-terminal boundary of the cluster."""
        return min(self.positions) if self.positions else 0

    @property
    def max_position(self) -> int:
        """C-terminal boundary of the cluster."""
        return max(self.positions) if self.positions else 0

    def overlaps_with(self, other: 'ClusterType', gap_tolerance: int = 0) -> bool:
        """
        Check if this cluster overlaps with (or is within gap_tolerance of) another.

        Args:
            other: Any cluster exposing min_position/max_position
            gap_tolerance: Maximum allowed gap to still count as overlapping

        Returns:
            True if the position ranges overlap or are within gap_tolerance
        """
        if not self.positions:
            return False
        self_min, self_max = self.min_position, self.max_position
        other_min, other_max = other.min_position, other.max_position
        return not (
            self_max + gap_tolerance < other_min or
            other_max + gap_tolerance < self_min
        )


@dataclass(frozen=True, slots=True)
class Cluster(_ClusterGeometryMixin):
    """
    Immutable representation of an aggregation-prone cluster identified by a single rule.

    Using frozen=True enables:
    - Hashability (can be used in sets, as dict keys)
    - Thread safety (no mutation possible after creation)
    - Semantic clarity (clusters are identified, not modified)

    Using slots=True reduces memory footprint by ~40% per instance compared
    to standard dataclass with __dict__.

    Attributes:
        positions: 1-indexed positions of residues in the cluster (must be sorted)
        residues: Amino acid single-letter codes at each position
        rule_name: Name of the rule that identified this cluster
        aggregation_score: Integer score indicating aggregation propensity weight

    Raises:
        ValueError: If positions and residues have different lengths,
                   if positions are empty, or if positions are not in sorted order
    """
    positions: Tuple[int, ...]
    residues: Tuple[str, ...]
    rule_name: str
    aggregation_score: int

    def __post_init__(self):
        """Validate cluster invariants after initialization."""
        if len(self.positions) != len(self.residues):
            raise ValueError(
                f"Position count ({len(self.positions)}) must match "
                f"residue count ({len(self.residues)})"
            )
        if not self.positions:
            raise ValueError("Cluster must contain at least one position")

        if any(p < 1 for p in self.positions):
            raise ValueError("Positions must be 1-indexed (≥1)")
        if list(self.positions) != sorted(self.positions):
            raise ValueError("Positions must be in sorted order")

    @property
    def density(self) -> float:
        """
        Cluster density: occupied positions / total span.

        Higher density indicates tighter packing of aggregation-prone
        residues in the primary sequence, which correlates with
        stronger aggregation propensity.
        """
        return self.size / self.span if self.span > 0 else 0.0

    @property
    def motif(self) -> str:
        """The cluster residues concatenated as a sequence motif."""
        return ''.join(self.residues)

    @property
    def mean_propensity(self) -> float:
        """
        Average intrinsic aggregation propensity of cluster residues.

        Uses the Tartaglia et al. (2008) aggregation propensity scale.
        Higher values indicate stronger predicted aggregation tendency.
        """
        propensities = [AGGREGATION_PROPENSITY.get(aa, 0.0) for aa in self.residues]
        return sum(propensities) / len(propensities) if propensities else 0.0

    def to_dict(self) -> Dict[str, Any]:
        """
        Convert to dictionary for serialization and backward compatibility.

        Returns:
            Dictionary with all cluster properties
        """
        return {
            'positions': list(self.positions),
            'residues': list(self.residues),
            'size': self.size,
            'span': self.span,
            'rule_name': self.rule_name,
            'aggregation_score': self.aggregation_score,
            'density': self.density,
            'mean_propensity': self.mean_propensity,
        }


@dataclass(frozen=True, slots=True)
class HydrophobicAromaticCluster(_ClusterGeometryMixin):
    """
    Specialized cluster for hydrophobic-aromatic interaction patterns.

    Extends the standard Cluster with information about:
    - Adjacent hydrophobic-aromatic pairs
    - Nearby hydrophobic residues that stabilize interactions
    - Sequence distances of those nearby hydrophobics (for validation
      of van der Waals contact feasibility)
    - Classification by formation mechanism

    This cluster type captures π-alkyl interactions between aromatic
    rings (F, Y, W) and aliphatic side chains (V, I, L, A, M),
    which are common in amyloid-forming sequences.

    Attributes:
        positions: 1-indexed positions of all involved residues
        residues: Amino acid codes at each position
        rule_name: Rule identifier (always 'hydrophobic_and_aromatic')
        aggregation_score: Weight for aggregation contribution
        pair_count: Number of hydrophobic-aromatic adjacent pairs
        condition: Formation mechanism ('at_least_2_pairs' or '1_pair_plus_hydrophobic')
        hydrophobic_count: Number of hydrophobic residues (V, I, L, A, M)
        aromatic_count: Number of aromatic residues (F, Y, W)
        pair_interactions: String descriptions of each pair (e.g., "V10-F11")
        nearby_hydrophobic_positions: Additional hydrophobic residues within 3 positions
        nearby_hydrophobic_distances: Parallel tuple of sequence distances for each
            nearby hydrophobic position
    """
    positions: Tuple[int, ...]
    residues: Tuple[str, ...]
    rule_name: str
    aggregation_score: int
    pair_count: int = 0
    condition: str = ""  # 'at_least_2_pairs' or '1_pair_plus_hydrophobic'
    hydrophobic_count: int = 0
    aromatic_count: int = 0
    pair_interactions: Tuple[str, ...] = ()
    nearby_hydrophobic_positions: Tuple[int, ...] = ()
    nearby_hydrophobic_distances: Tuple[int, ...] = ()  # Parallel tuple of distances

    def to_dict(self) -> Dict[str, Any]:
        """
        Convert to dictionary for serialization and output.

        Preserves per-pair interactions and, for each nearby hydrophobic
        residue, its sequence distance and residue identity.

        Returns:
            Dictionary with all cluster properties including pair details
        """
        nearby_hydrophobics = []
        for i, pos in enumerate(self.nearby_hydrophobic_positions):
            distance = (
                self.nearby_hydrophobic_distances[i]
                if i < len(self.nearby_hydrophobic_distances)
                else 0
            )
            residue = '?'
            if pos in self.positions:
                idx = self.positions.index(pos)
                residue = self.residues[idx]
            nearby_hydrophobics.append({
                'position': pos,
                'distance': distance,
                'residue': residue,
            })

        return {
            'positions': list(self.positions),
            'residues': list(self.residues),
            'size': self.size,
            'span': self.span,
            'rule_name': self.rule_name,
            'aggregation_score': self.aggregation_score,
            'pair_count': self.pair_count,
            'condition': self.condition,
            'hydrophobic_count': self.hydrophobic_count,
            'aromatic_count': self.aromatic_count,
            'pairs': [{'interaction': p} for p in self.pair_interactions],
            'nearby_hydrophobics': nearby_hydrophobics,
        }


@dataclass(frozen=True, slots=True)
class MultiRuleCluster(_ClusterGeometryMixin):
    """
    Represents a cluster matching multiple aggregation rules simultaneously.

    These clusters indicate regions with compounded aggregation risk
    where multiple physicochemical features converge (e.g., both
    hydrophobic aliphatic and aromatic clustering in the same region).

    Multi-rule clusters receive boosted aggregation scores reflecting
    their increased risk compared to single-rule clusters.

    Attributes:
        positions: 1-indexed positions of all residues in merged cluster
        residues: Amino acid codes at each position
        rules: Tuple of rule names that converged at this region
        combined_aggregation_score: Boosted score (sum of individual scores + bonus)

    Raises:
        ValueError: If position/residue counts mismatch or fewer than 2 rules
    """
    positions: Tuple[int, ...]
    residues: Tuple[str, ...]
    rules: Tuple[str, ...]
    combined_aggregation_score: int

    def __post_init__(self):
        """Validate multi-rule cluster invariants."""
        if len(self.positions) != len(self.residues):
            raise ValueError("Position and residue counts must match")
        if len(self.rules) < 2:
            raise ValueError(
                f"MultiRuleCluster requires at least 2 rules, got {len(self.rules)}"
            )

    @property
    def is_multi_rule(self) -> bool:
        """Always True for MultiRuleCluster; aids duck typing with Cluster."""
        return True

    @property
    def rule_string(self) -> str:
        """Formatted string of all rule names joined by '+'."""
        return '+'.join(sorted(self.rules))

    @property
    def aggregation_score(self) -> int:
        """Alias for combined_aggregation_score for duck typing compatibility."""
        return self.combined_aggregation_score

    @property
    def rule_name(self) -> str:
        """Alias for rule_string for duck typing compatibility."""
        return self.rule_string

    def to_dict(self) -> Dict[str, Any]:
        """
        Convert to dictionary for serialization.

        Returns:
            Dictionary with all multi-rule cluster properties
        """
        return {
            'positions': list(self.positions),
            'residues': list(self.residues),
            'size': self.size,
            'span': self.span,
            'rules': list(self.rules),
            'combined_aggregation_score': self.combined_aggregation_score,
            'is_multi_rule': True,
        }


# Type alias for any cluster type
ClusterType = Union[Cluster, HydrophobicAromaticCluster, MultiRuleCluster]


