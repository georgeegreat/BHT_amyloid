@dataclass
class MutationInfo:
    """
    Structured representation of a single mutation with full biological context.

    Encapsulates all information about a mutation including its structural
    context (gatekeeper, core, flanking), aggregation impact, and
    the rule(s) that triggered it.

    Attributes:
        position: 1-indexed position in the sequence
        original_aa: Single-letter code of the original amino acid
        new_aa: Single-letter code of the new amino acid
        mutation_type: Classification of the mutation's structural context
        aggregation_score: Impact score for aggregation propensity
        region: Optional region string (format: "start:stop")
        rule_name: Name of the rule that triggered this mutation
        distance_to_apr_boundary: Distance to nearest APR boundary
        sequence: The full mutated sequence
        description: Formatted description string for FASTA output
    """
    position: int
    original_aa: str
    new_aa: str
    mutation_type: MutationType
    aggregation_score: int
    region: Optional[str] = None
    rule_name: Optional[str] = None
    distance_to_apr_boundary: Optional[int] = None
    sequence: str = ""
    description: str = ""

    @property
    def is_gatekeeper(self) -> bool:
        """Check if this mutation is at a gatekeeper position."""
        return self.mutation_type == MutationType.GATEKEEPER

    @property
    def is_core(self) -> bool:
        """Check if this mutation is within an APR core."""
        return self.mutation_type == MutationType.BETA_CORE

    def to_tuple(self) -> Tuple[str, str, int]:
        """
        Convert to legacy tuple format for backward compatibility.

        Returns:
            Tuple of (description, sequence, aggregation_score)
        """
        return (self.description, self.sequence, self.aggregation_score)


@dataclass
class MultiMutationResult:
    """
    Structured result for multi-point mutation combinations.

    Represents a sequence variant with mutations at multiple positions,
    including classification of the combined mutation types and regions.

    Attributes:
        description: Formatted description for FASTA header
        sequence: Full mutated sequence with all changes applied
        aggregation_score: Combined aggregation impact score
        positions: List of mutated positions
        regions: List of region strings involved
        level: Number of simultaneous mutations (2=double, 3=triple, etc.)
        mutation_types: Tuple of MutationType for each position
        type_composition: Tuple of type name strings
        original_aas: Original amino acids at each position
        new_aas: New amino acids at each position
    """
    description: str
    sequence: str
    aggregation_score: int
    positions: List[int]
    regions: List[str]
    level: int
    mutation_types: Tuple[MutationType, ...]
    type_composition: Tuple[str, ...]
    original_aas: Tuple[str, ...] = ()
    new_aas: Tuple[str, ...] = ()

    @property
    def is_all_gatekeeper(self) -> bool:
        """Check if all component mutations are gatekeepers."""
        return all(mt == MutationType.GATEKEEPER for mt in self.mutation_types)

    @property
    def is_all_core(self) -> bool:
        """Check if all component mutations are in β-core regions."""
        return all(mt == MutationType.BETA_CORE for mt in self.mutation_types)

    @property
    def is_mixed(self) -> bool:
        """Check if mutations span different structural contexts."""
        return len(set(self.mutation_types)) > 1

    @property
    def is_single_region(self) -> bool:
        """Check if all mutations are in the same region."""
        return len(set(self.regions)) <= 1

    def to_dict(self) -> Dict[str, Any]:
        """
        Convert to dictionary for serialization and output.

        Returns:
            Dictionary with all multi-mutation properties
        """
        return {
            'description': self.description,
            'sequence': self.sequence,
            'agg_score': self.aggregation_score,
            'positions': self.positions,
            'regions': self.regions,
            'level': self.level,
            'mutation_types': self.mutation_types,
            'type_composition': self.type_composition,
            'is_all_gatekeeper': self.is_all_gatekeeper,
            'is_all_core': self.is_all_core,
            'is_mixed': self.is_mixed,
        }

    def to_tuple(self) -> Tuple[str, str, int]:
        """
        Convert to legacy tuple format (description, sequence, score).

        Returns:
            Tuple of (description, sequence, aggregation_score)
        """
        return (self.description, self.sequence, self.aggregation_score)